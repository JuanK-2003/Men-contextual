    void data_Table(){
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        table.setRowCount(0);
        for(int i = 0; i < c; i++){
            table.addRow(P.get(i).getinfo());
        }
        jTable1.setModel(table);
    }
    void clean_data(){
        jTextField1.setText("");
        jTextField2.setText("");
        jDateChooser2.setDate(null);
    }
    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(!jTextField2.getText().isEmpty() && !jTextField1.getText().isEmpty() && !editar){
            P.add(c, new Persona(jTextField2.getText(),jTextField1.getText(),jDateChooser2.getDate()));
            c++;
        }
        else if (editar){
            P.get(in).setFecha_Naci(jDateChooser2.getDate());
            P.get(in).setDPI(jTextField2.getText());
            P.get(in).setNombre(jTextField1.getText());
            P.get(in).Calcular_Edad();
            editar = false;
            jButton1.setText("AGREGAR DATO");
        }
        this.data_Table();
        this.clean_data();
    }//GEN-LAST:event_jButton1MouseClicked

    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        if(jTable1.getSelectedRow() >= 0){
            jButton1.setText("EDITAR");
            editar = true;
            in = jTable1.getSelectedRow();
            jTextField1.setText(P.get(in).getNombre());
            jTextField2.setText(P.get(in).getDPI());
            jDateChooser2.setDate(P.get(in).getFecha_Naci());
        }
    }//GEN-LAST:event_jMenuItem4ActionPerformed

